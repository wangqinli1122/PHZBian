<template>
  <div>
    <shops-header></shops-header>
    <shops-banner :img="shopImg"></shops-banner>
    <shops-info
      :name="shopName"
      :address="shopAddress"
      :range="shopRange"
      :tel="shopTel">
    </shops-info>
    <h3 class="main-title">店铺商品</h3>
    <shops-goods :list="goods"></shops-goods>
  </div>
</template>

<script>
import ShopsHeader from './components/Header'
import ShopsBanner from './components/Banner'
import ShopsInfo from './components/Info'
import ShopsGoods from './components/Goods'
import axios from 'axios'
export default {
  name: 'Shops',
  data () {
    return {
      shopName: '',
      shopAddress: '',
      shopRange: '',
      shopTel: '',
      shopImg: '',
      goods: []
    }
  },
  components: {
    ShopsHeader,
    ShopsBanner,
    ShopsInfo,
    ShopsGoods
  },
  methods: {
    getHomeInfo () {
      axios.get('/api/shops.json').then(this.getHomeInfoSucc)
    },
    getHomeInfoSucc (res) {
      res = res.data
      if (res.ret === true) {
        const data = res.data
        this.goods = data.goodsList
        this.shopName = data.shopName
        this.shopAddress = data.shopAddress
        this.shopRange = data.shopRange
        this.shopTel = data.shopTel
        this.shopImg = data.shopImg
      }
    }
  },
  mounted () {
    this.getHomeInfo()
  }
}
</script>

